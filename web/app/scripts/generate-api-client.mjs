import { execFileSync } from "node:child_process";
import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const scriptDir = dirname(fileURLToPath(import.meta.url));
const appDir = resolve(scriptDir, "..");
const repoRoot = resolve(appDir, "../..");
const generatedDir = resolve(appDir, "src/generated");
const openapiPath = resolve(generatedDir, "openapi.json");
const clientPath = resolve(generatedDir, "api-client.ts");

mkdirSync(generatedDir, { recursive: true });

const fromFileArg = process.argv.find((arg) => arg.startsWith("--from-file="));
const openapiText = fromFileArg
  ? readFileSync(resolve(process.cwd(), fromFileArg.slice("--from-file=".length)), "utf8")
  : execFileSync("cargo", ["run", "--quiet", "-p", "pygco-api", "--example", "openapi"], {
      cwd: repoRoot,
      encoding: "utf8",
      stdio: ["ignore", "pipe", "inherit"]
    });

const openapi = JSON.parse(openapiText);
writeFileSync(openapiPath, `${JSON.stringify(openapi, null, 2)}\n`);
writeFileSync(clientPath, generateClient(openapi));

function generateClient(document) {
  const schemas = document.components?.schemas ?? {};
  const typeBlocks = Object.keys(schemas)
    .sort()
    .map((name) => {
      const source = schemas[name]?.["x-typescript"];
      if (!source) {
        throw new Error(`Schema ${name} is missing x-typescript`);
      }
      return source.trim();
    })
    .join("\n\n");

  const operations = collectOperations(document);
  const methods = operations.map(generateMethod).join("\n\n");

  return `// Generated by scripts/generate-api-client.mjs from /api/openapi.json.
// Do not edit by hand.

export type ApiEnvelope<T> = { data: T; meta?: Record<string, unknown> };
export type ApiResult<T> = { data: T; meta: Record<string, unknown> };
export type ApiErrorPayload = { code?: string; message?: string; details?: Record<string, unknown> };

${typeBlocks}

export class PygcoApiError extends Error {
  code: string;
  details: Record<string, unknown>;

  constructor(payload: ApiErrorPayload, fallback: string) {
    super(payload.message ?? fallback);
    this.name = "PygcoApiError";
    this.code = payload.code ?? "request_failed";
    this.details = payload.details ?? {};
  }
}

type FetchLike = typeof fetch;

export class PygcoApiClient {
  private readonly baseUrl: string;
  private readonly fetchImpl: FetchLike;

  constructor(baseUrl = "", fetchImpl: FetchLike = globalThis.fetch.bind(globalThis)) {
    this.baseUrl = baseUrl.replace(/\\/$/, "");
    this.fetchImpl = fetchImpl;
  }

${indent(methods, 2)}

  private async requestData<T>(path: string, init?: RequestInit): Promise<T> {
    return this.requestEnvelope<T>(path, init).then((payload) => payload.data);
  }

  private async requestEnvelope<T>(path: string, init?: RequestInit): Promise<ApiResult<T>> {
    const response = await this.fetchImpl(this.url(path), {
      ...init,
      headers: {
        "content-type": "application/json",
        ...(init?.headers ?? {})
      }
    });
    const text = await response.text();
    const payload = text ? JSON.parse(text) : {};
    if (!response.ok) {
      throw new PygcoApiError(payload.error ?? {}, response.statusText);
    }
    const envelope = payload as ApiEnvelope<T>;
    return { data: envelope.data, meta: envelope.meta ?? {} };
  }

  private async requestText(path: string, init?: RequestInit): Promise<string> {
    const response = await this.fetchImpl(this.url(path), init);
    const text = await response.text();
    if (!response.ok) {
      let payload: { error?: ApiErrorPayload } = {};
      try {
        payload = text ? JSON.parse(text) : {};
      } catch {
        payload = {};
      }
      throw new PygcoApiError(payload.error ?? {}, response.statusText);
    }
    return text;
  }

  private url(path: string) {
    return \`\${this.baseUrl}\${path}\`;
  }
}

export const apiClient = new PygcoApiClient();

function withQuery(path: string, params?: Record<string, unknown>) {
  if (!params) return path;
  const query = new URLSearchParams();
  for (const [key, value] of Object.entries(params)) {
    if (value === undefined || value === null) continue;
    query.set(key, String(value));
  }
  const serialized = query.toString();
  return serialized ? \`\${path}?\${serialized}\` : path;
}
`;
}

function collectOperations(document) {
  const operations = [];
  for (const [path, pathItem] of Object.entries(document.paths ?? {})) {
    for (const [httpMethod, operation] of Object.entries(pathItem)) {
      const client = operation?.["x-client"];
      if (!client) continue;
      operations.push({
        path,
        httpMethod: httpMethod.toUpperCase(),
        operationId: operation.operationId,
        ...client
      });
    }
  }
  return operations.sort((left, right) => left.methodName.localeCompare(right.methodName));
}

function generateMethod(operation) {
  const pathParams = operation.pathParams ?? [];
  const args = pathParams.map((param) => `${param.name}: ${param.type === "number" ? "number" : "string"}`);
  if (operation.querySchema) {
    args.push(`params${schemaHasRequired(operation.querySchema) ? "" : "?"}: ${operation.querySchema}`);
  }
  if (operation.bodySchema) {
    args.push(`body: ${operation.bodySchema}`);
  }

  const pathExpr = pathParams.reduce((expr, param) => {
    return `${expr}.replace("{${param.placeholder}}", encodeURIComponent(String(${param.name})))`;
  }, JSON.stringify(operation.path));
  const withQueryExpr = operation.querySchema ? `withQuery(${pathExpr}, params)` : pathExpr;
  const init = operation.httpMethod === "GET" ? "" : `, { method: "${operation.httpMethod}", body: JSON.stringify(${operation.bodySchema ? "body" : "{}"}) }`;
  const requester = operation.responseMode === "envelope"
    ? "requestEnvelope"
    : operation.responseMode === "text"
      ? "requestText"
      : "requestData";
  const generic = requester === "requestText" ? "" : `<${operation.responseSchema}>`;

  return `${operation.methodName}(${args.join(", ")}): Promise<${operation.responseMode === "envelope" ? `ApiResult<${operation.responseSchema}>` : operation.responseSchema}> {
  return this.${requester}${generic}(${withQueryExpr}${init});
}`;
}

function schemaHasRequired(schemaName) {
  return [
    "DiffObjectsParams",
    "DiffParams",
    "GraphParams"
  ].includes(schemaName);
}

function indent(text, spaces) {
  const prefix = " ".repeat(spaces);
  return text.split("\n").map((line) => (line ? `${prefix}${line}` : line)).join("\n");
}
